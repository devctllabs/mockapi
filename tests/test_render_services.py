    def test_project_renderer_supports_id_counter_state_slice(self) -> None:
        profile = profile_model(
            profile_toml()
            + """\

            [[state.slices]]
            name = "idCounters"
            recordType = "Record<string, number>"
            array = false
            """
        )
        context = create_generate_context(profile, PROJECT_ROOT)

        writes = ProjectRenderService(self.fs, self.template_service, ADMIN_OPENAPI_TEMPLATE_PATH).planned_writes(context)
        by_path = {write.path.relative_to(context.outRoot).as_posix(): write for write in writes}
        admin_openapi = by_path["openapi/admin.source.yaml"].content
        seed = by_path["src/generated/mock-admin/state/seed.ts"].content

        self.assertIn("        - idCounters", admin_openapi)
        self.assertIn(
            "        idCounters:\n"
            "          type: object\n"
            "          additionalProperties:\n"
            "            type: number",
            admin_openapi,
        )
        self.assertNotIn("IdCountersRecord:", admin_openapi)
        self.assertIn('    idCounters: {} as MockState["idCounters"],', seed)

    def test_feature_renderer_skips_existing_operation_files(self) -> None:
        profile = profile_model(profile_toml())
        context = create_generate_context(profile, PROJECT_ROOT)
        feature_root = PROJECT_ROOT / "mock-server/src/features/workspaces"
        self.fs.write_text(feature_root / "controllers/listWorkspaces.ts", "// custom\n")

        writes = FeatureRenderService(self.fs, self.template_service).planned_writes(context)

        paths = {write.path for write in writes}
        self.assertNotIn(feature_root / "controllers/listWorkspaces.ts", paths)
        self.assertNotIn(feature_root / "services/listWorkspaces.ts", paths)
        self.assertNotIn(feature_root / "repository.ts", paths)
        self.assertIn(feature_root / "seed.ts", paths)

    def test_feature_renderer_generates_only_operation_controllers(self) -> None:
        profile = profile_model(profile_toml())
        context = create_generate_context(profile, PROJECT_ROOT)
        feature_root = PROJECT_ROOT / "mock-server/src/features/workspaces"

        writes = FeatureRenderService(self.fs, self.template_service).planned_writes(context)

        paths = {write.path for write in writes}
        by_path = {write.path: write for write in writes}
        self.assertIn(feature_root / "controllers/listWorkspaces.ts", paths)
        self.assertIn(feature_root / "seed.ts", paths)
        self.assertNotIn(feature_root / "services/listWorkspaces.ts", paths)
        self.assertNotIn(feature_root / "repository.ts", paths)
        self.assertIn('Pick<ProductMockControllers, "listWorkspaces">', by_path[feature_root / "controllers/listWorkspaces.ts"].content)
        self.assertIn("listWorkspaces: async", by_path[feature_root / "controllers/listWorkspaces.ts"].content)
        self.assertNotIn('"listWorkspaces": async', by_path[feature_root / "controllers/listWorkspaces.ts"].content)
        self.assertIn("  workspaces: [],", by_path[feature_root / "seed.ts"].content)
        self.assertNotIn('  "workspaces": [],', by_path[feature_root / "seed.ts"].content)

    def test_feature_renderer_skips_existing_seed_file(self) -> None:
        profile = profile_model(profile_toml())
        context = create_generate_context(profile, PROJECT_ROOT)
        feature_root = PROJECT_ROOT / "mock-server/src/features/workspaces"
        self.fs.write_text(feature_root / "seed.ts", "// custom seed\n")

        writes = FeatureRenderService(self.fs, self.template_service).planned_writes(context)

        paths = {write.path for write in writes}
        self.assertNotIn(feature_root / "seed.ts", paths)

    def test_feature_renderer_does_not_generate_feature_modules_without_state_slices(self) -> None:
        profile = profile_model(profile_toml().replace('stateSlices = ["workspaces"]\n', ""))
        context = create_generate_context(profile, PROJECT_ROOT)
        feature_root = PROJECT_ROOT / "mock-server/src/features/workspaces"

        writes = FeatureRenderService(self.fs, self.template_service).planned_writes(context)

        paths = {write.path for write in writes}
        self.assertNotIn(feature_root / "repository.ts", paths)
        self.assertNotIn(feature_root / "seed.ts", paths)


